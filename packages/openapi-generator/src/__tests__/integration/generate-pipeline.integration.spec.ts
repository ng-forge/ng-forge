import { writeFile, mkdir } from 'node:fs/promises';
import { join } from 'node:path';
import { runGenerateAction } from '../../cli/commands/generate.command.js';
import { createTempDir, cleanupDir, fixturePath, readGenerated, readConfigFile, fileExists, typecheckGeneratedForm } from './helpers.js';

let outputDir: string;
let exitSpy: ReturnType<typeof vi.spyOn>;

beforeEach(async () => {
  outputDir = await createTempDir();
  exitSpy = vi.spyOn(process, 'exit').mockImplementation((() => {
    // no-op: prevent process from actually exiting
  }) as never);
});

afterEach(async () => {
  await cleanupDir(outputDir);
  exitSpy.mockRestore();
});

function generate(fixture: string, overrides: Record<string, unknown> = {}) {
  return runGenerateAction({
    spec: fixturePath(fixture),
    output: outputDir,
    interactive: 'none',
    ...overrides,
  } as Parameters<typeof runGenerateAction>[0]);
}

// ─── A. Basic Pipeline ───────────────────────────────────────────────

describe('Basic Pipeline', () => {
  it('should generate form config, interface, and barrel files', async () => {
    await generate('basic-post.yaml');

    const form = await readGenerated(outputDir, 'forms', 'create-user.form.ts');
    expect(form).toContain("key: 'name'");
    expect(form).toContain("type: 'input'");
    expect(form).toContain('as const satisfies FormConfig');
    expect(form).toContain('// @generated by @ng-forge/openapi-generator');

    const types = await readGenerated(outputDir, 'types', 'create-user.types.ts');
    expect(types).toContain('export interface CreateUserFormValue');

    const formBarrel = await readGenerated(outputDir, 'forms', 'index.ts');
    expect(formBarrel).toContain("from './create-user.form'");
    expect(formBarrel).not.toContain('.js');

    const typeBarrel = await readGenerated(outputDir, 'types', 'index.ts');
    expect(typeBarrel).toContain("from './create-user.types'");
    expect(typeBarrel).not.toContain('.js');
  });

  it('should generate correct required/optional interface properties', async () => {
    await generate('basic-post.yaml');

    const types = await readGenerated(outputDir, 'types', 'create-user.types.ts');
    expect(types).toContain('name: string');
    expect(types).toContain('age?: number');
    expect(types).toContain('active?: boolean');
  });

  it('should save config file with endpoints and decisions', async () => {
    await generate('basic-post.yaml');

    const config = await readConfigFile(outputDir);
    expect(config['endpoints']).toEqual(['POST:/users']);
    expect(config['decisions']).toBeDefined();
  });

  it('should omit barrelExtension from config when using the default', async () => {
    await generate('basic-post.yaml');

    const config = await readConfigFile(outputDir);
    // Default (empty) state is representable as "field absent" so users can tell
    // apart "never configured" from "explicitly empty".
    expect(config['barrelExtension']).toBeUndefined();
  });

  it('should emit .js suffixes and persist barrelExtension when requested', async () => {
    await generate('basic-post.yaml', { barrelExtension: '.js' });

    const formBarrel = await readGenerated(outputDir, 'forms', 'index.ts');
    expect(formBarrel).toContain("from './create-user.form.js'");

    const config = await readConfigFile(outputDir);
    expect(config['barrelExtension']).toBe('.js');
  });

  it('should honor saved barrelExtension on re-run without the flag', async () => {
    // First run: opt into .js and persist.
    await generate('basic-post.yaml', { barrelExtension: '.js' });
    // Second run: no flag — must reuse the saved value, not fall back to default.
    await generate('basic-post.yaml');

    const formBarrel = await readGenerated(outputDir, 'forms', 'index.ts');
    expect(formBarrel).toContain("from './create-user.form.js'");

    const config = await readConfigFile(outputDir);
    expect(config['barrelExtension']).toBe('.js');
  });

  it('should clear saved barrelExtension when explicitly set to empty', async () => {
    // Prime a persisted .js value.
    await generate('basic-post.yaml', { barrelExtension: '.js' });
    // Explicit reset.
    await generate('basic-post.yaml', { barrelExtension: '' });

    const formBarrel = await readGenerated(outputDir, 'forms', 'index.ts');
    expect(formBarrel).toContain("from './create-user.form'");
    expect(formBarrel).not.toContain('.js');

    const config = await readConfigFile(outputDir);
    expect(config['barrelExtension']).toBeUndefined();
  });

  it('should produce a form file that compiles against the published types', async () => {
    await generate('basic-post.yaml');

    const formPath = join(outputDir, 'forms', 'create-user.form.ts');
    const diagnostics = typecheckGeneratedForm(formPath);

    expect(diagnostics, `Generated form should type-check cleanly. Errors:\n${diagnostics.join('\n')}`).toEqual([]);
  }, 30_000); // tsc program creation + type-check is slow in CI
});

