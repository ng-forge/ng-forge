// ─── I. Config Persistence ───────────────────────────────────────────

describe('Config Persistence', () => {
  it('should produce unchanged files on re-run with saved config', async () => {
    await generate('basic-post.yaml');
    const firstRun = await readGenerated(outputDir, 'forms', 'create-user.form.ts');

    // Re-run — config file already exists
    await generate('basic-post.yaml');
    const secondRun = await readGenerated(outputDir, 'forms', 'create-user.form.ts');

    expect(firstRun).toBe(secondRun);
  });

  it('should respect saved endpoint selections on re-run', async () => {
    // First run with specific endpoint filter
    await generate('multi-endpoint.yaml', { endpoints: 'POST:/pets' });

    const config = await readConfigFile(outputDir);
    expect(config['endpoints']).toEqual(['POST:/pets']);

    // Second run without --endpoints flag — should use saved config
    await generate('multi-endpoint.yaml');

    // Should still only have the one form from the saved selection
    const exists = await fileExists(join(outputDir, 'forms', 'create-pet.form.ts'));
    expect(exists).toBe(true);
  });
});

// ─── J. CLI Flags ────────────────────────────────────────────────────

describe('CLI Flags', () => {
  it('should not write files with --dry-run', async () => {
    await generate('basic-post.yaml', { dryRun: true });

    const formsExist = await fileExists(join(outputDir, 'forms'));
    expect(formsExist).toBe(false);
  });

  it('should preserve existing files with --skip-existing', async () => {
    // Pre-create a file
    await mkdir(join(outputDir, 'forms'), { recursive: true });
    await writeFile(join(outputDir, 'forms', 'create-user.form.ts'), 'CUSTOM CONTENT');

    await generate('basic-post.yaml', { skipExisting: true });

    const content = await readGenerated(outputDir, 'forms', 'create-user.form.ts');
    expect(content).toBe('CUSTOM CONTENT');
  });

  it('should filter to selected endpoints only with --endpoints', async () => {
    await generate('multi-endpoint.yaml', { endpoints: 'POST:/pets' });

    const createPet = await fileExists(join(outputDir, 'forms', 'create-pet.form.ts'));
    const updatePet = await fileExists(join(outputDir, 'forms', 'update-pet.form.ts'));

    expect(createPet).toBe(true);
    expect(updatePet).toBe(false);
  });
});

