#!/usr/bin/env node
/**
 * Generate internal/src/public_api.ts: `export *` from every non-spec module in
 * internal/src/lib. Same-binding re-export duplicates are fine; only genuinely
 * distinct same-name exports collide (the build will name them, exclude as needed).
 */
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'node:fs';
import { join, relative, resolve } from 'node:path';

const PKG = resolve('packages/dynamic-forms');
const LIB = join(PKG, 'internal/src/lib');
const OUT = join(PKG, 'internal/src/public_api.ts');
const EXCLUDE = new Set(); // module relpaths (no ext) to skip on collision

const isSkip = (f) => f.endsWith('.spec.ts') || f.endsWith('.type-test.ts');
const hasExport = (f) => /\bexport\b/.test(readFileSync(f, 'utf8'));

function walk(d, a = []) {
  for (const n of readdirSync(d)) {
    const p = join(d, n);
    if (statSync(p).isDirectory()) walk(p, a);
    else if (p.endsWith('.ts') && !isSkip(p)) a.push(p);
  }
  return a;
}

const mods = walk(LIB)
  .filter(hasExport)
  .map((f) => './lib/' + relative(LIB, f).replace(/\.ts$/, '').replaceAll('\\', '/'))
  .filter((m) => !EXCLUDE.has(m))
  .sort();

const header = `/**
 * @ng-forge/dynamic-forms/internal
 *
 * BUILD-TIME FOUNDATION ENTRYPOINT — not a supported public API, no semver guarantee.
 * Shared compiled DI tokens / services / config types for the consumer
 * (\`@ng-forge/dynamic-forms\`) and adapter-author (\`@ng-forge/dynamic-forms/integration\`)
 * entrypoints, so tokens keep a single identity across bundles.
 *
 * Form consumers  -> '@ng-forge/dynamic-forms'
 * Adapter authors -> '@ng-forge/dynamic-forms/integration'
 *
 * AUTO-GENERATED by scripts/refactor/gen-public-api.mjs — do not edit by hand.
 */\n\n`;

writeFileSync(OUT, header + mods.map((m) => `export * from '${m}';`).join('\n') + '\n');
console.log(`generated ${mods.length} export lines`);
